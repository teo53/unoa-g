/// KRDS-inspired 8pt grid spacing system
///
/// UNO A 디자인 토큰 - 간격 상수
/// KRDS 레이아웃 가이드라인 (8pt 그리드) 기반
class AppSpacing {
  AppSpacing._();

  /// 4px - 아이콘 내부 간격, 미세 조정
  static const double xs = 4;

  /// 8px - 작은 요소 간격, 인라인 패딩
  static const double sm = 8;

  /// 12px - 컴포넌트 내부 패딩, 리스트 아이템 간격
  static const double md = 12;

  /// 16px - 기본 컨텐츠 패딩, 섹션 내 간격
  static const double lg = 16;

  /// 20px - 섹션 간 간격
  static const double xl = 20;

  /// 24px - 화면 좌우 패딩, 큰 섹션 간격
  static const double xxl = 24;

  /// 32px - 섹션 구분, 큰 여백
  static const double xxxl = 32;

  /// 40px - 대형 섹션 간격
  static const double xxxxl = 40;

  /// 48px - 최대 간격
  static const double xxxxxl = 48;
}
